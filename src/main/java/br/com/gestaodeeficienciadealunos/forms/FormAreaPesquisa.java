    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        
        if(campo_codigo.getText().equals(""))
        {
            ap.setCodigo(0);
        }
        else
        {
            ap.setCodigo(Integer.parseInt(campo_codigo.getText()));
        }
        ap.setAreaPesquisa(campo_area_pesquisa.getText());
        ap.setDescricao(campo_descricao.getText());
        
        try
        {
            ap.consultar(ap);
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_consultarActionPerformed

    private void btn_resetarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_resetarActionPerformed
        try
        {
            ap.resetTable();
            campo_codigo.setText("");
            campo_area_pesquisa.setText("");
            campo_descricao.setText("");
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_resetarActionPerformed

